dump_fib(SIZE) :-
    フィボナッチ数列表示(SIZE).
フィボナッチ数列表示(_数列長) :-
    フィボナッチ数列計算(_数列長, _結果数列),
    write(_結果数列), nl.
フィボナッチ数列計算(_数列長, _結果数列) :-
    _結果数列 = [0,1|_結果数列2],
    フィボナッチ数列計算(0, 1, _数列長 - 2, _結果数列2).
フィボナッチ数列計算(A, B, _数列長, _結果数列) :-
    C is A + B,
    (
	_数列長_1 is  _数列長 - 1,
	0 =<_数列長_1
     ->
	 _結果数列 = [C | _結果数列2],
	 フィボナッチ数列計算(B, C, _数列長_1, _結果数列2)
     ;
     !, _結果数列 = []
    ).
